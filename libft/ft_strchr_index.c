#include "libft.h"

int	ft_strchr_index(char const *s, char c)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return (i);
		i++;
	}
	if (s[i] == '\0' && c == '\0')
		return (i);
	return (-1);
}
